CIceHard::~CIceHard()
{

}

//=====================================================
// 初期化処理
//=====================================================
HRESULT CIceHard::Init(void)
{
	CIce::Init();

	CFan3D *pFan3D = CIce::GetFan3D();

	if (pFan3D == nullptr)
		return E_FAIL;

	int nIdxTexture = Texture::GetIdx(&PATH_TEX[0]);
	pFan3D->SetIdxTexture(nIdxTexture);

	return S_OK;
}

